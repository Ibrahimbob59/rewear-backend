<?php

namespace App\Http\Resources;

use Illuminate\Http\Request;
use Illuminate\Http\Resources\Json\JsonResource;

class UserResource extends JsonResource
{
    /**
     * Transform the resource into an array.
     *
     * @return array<string, mixed>
     */
    public function toArray(Request $request): array
    {
        return [
            'id' => $this->id,
            'name' => $this->name,
            'email' => $this->email,
            'phone' => $this->phone,
            'user_type' => $this->user_type,
            'driver_verified' => $this->driver_verified ?? false,
            'driver_verified_at' => $this->driver_verified_at?->toISOString(),
            'created_at' => $this->created_at->toISOString(),
            'updated_at' => $this->updated_at->toISOString(),

            // Include driver application if loaded (latest application)
            'driver_application' => $this->whenLoaded('driverApplication', function () {
                return DriverApplicationResource::collection($this->driverApplication);
            }),
        ];
    }
}
